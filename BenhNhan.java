package QLBN;

import java.io.Serializable;

/**
 *
 * @author Administrator
 */
public class BenhNhan implements Serializable{
    private String maBA;
    private String hoTen;
    private String gioiTinh;
    private String ngaySinh;
    private String chanDoan;
    private String ngayNhapVien;
    private String tinhTrang;

    public BenhNhan() {
    }

    public BenhNhan(String maBA, String hoTen, String gioiTinh, String ngaySinh, String chanDoan, String ngayNhapVien, String tinhTrang) {
        this.maBA = maBA;
        this.hoTen = hoTen;
        this.gioiTinh = gioiTinh;
        this.ngaySinh = ngaySinh;
        this.chanDoan = chanDoan;
        this.ngayNhapVien = ngayNhapVien;
        this.tinhTrang = tinhTrang;
    }

    public String getMaBA() {
        return maBA;
    }

    public void setMaBA(String maBA) {
        this.maBA = maBA;
    }

    public String getHoTen() {
        return hoTen;
    }

    public void setHoTen(String hoTen) {
        this.hoTen = hoTen;
    }

    public String getGioiTinh() {
        return gioiTinh;
    }

    public void setGioiTinh(String gioiTinh) {
        this.gioiTinh = gioiTinh;
    }

    public String getNgaySinh() {
        return ngaySinh;
    }

    public void setNgaySinh(String ngaySinh) {
        this.ngaySinh = ngaySinh;
    }

    public String getChanDoan() {
        return chanDoan;
    }

    public void setChanDoan(String chanDoan) {
        this.chanDoan = chanDoan;
    }

    public String getNgayNhapVien() {
        return ngayNhapVien;
    }

    public void setNgayNhapVien(String ngayNhapVien) {
        this.ngayNhapVien = ngayNhapVien;
    }

    public String getTinhTrang() {
        return tinhTrang;
    }

    public void setTinhTrang(String tinhTrang) {
        this.tinhTrang = tinhTrang;
    }
    
}
